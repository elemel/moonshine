local mimic = require "mimic"

local level_map = [[
###############################################################################
#                                                                             #
#                                                                             #
#                                                                             #
#                                                    ##########               #
#                  ########                          #                        #
#                         #                          #                        #
#                         #                          ################         #
#                         #                                         #         #
#      #                  #                                         #         #
#      #                                                            #         #
#      #                                  ###########               #         #
#      #                                  #         #                         #
