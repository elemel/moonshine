#           #                                       #                         #
#           #                                       #                         #
#           #                                       #                         #
#           #                                                                 #
#                                                                             #
#                                                                             #
#                                                                             #
###############################################################################
]]

function new_level()
    local level = {}
    level.grid = mimic.array(mimic.map(mimic.split(level_map, "\n"), mimic.array))
    return level
end
